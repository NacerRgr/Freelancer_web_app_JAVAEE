package javaBeans;

public class evaluationProf {
	private String  cinClient;
	private String  cinProf;
	private String  progresP;
	private String description;
	private String nom;
	private String prenom;
	
	private int nbrEtoileP;
	private int nbrEtoileC;
	public String getCinProf() {
		return cinProf;
	}
	public void setCinProf(String cinProf) {
		this.cinProf = cinProf;
	}
	public String getProgresP() {
		return progresP;
	}
	public void setProgresP(String progresP) {
		this.progresP = progresP;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public int getNbrEtoileP() {
		return nbrEtoileP;
	}
	public void setNbrEtoileP(int nbrEtoileP) {
		this.nbrEtoileP = nbrEtoileP;
	}
	public int getNbrEtoileC() {
		return nbrEtoileC;
	}
	public void setNbrEtoileC(int nbrEtoileC) {
		this.nbrEtoileC = nbrEtoileC;
	}
	public String getCinClient() {
		return cinClient;
	}
	public void setCinClient(String cinClient) {
		this.cinClient = cinClient;
	}
	public String getNom() {
		return nom;
	}
	public void setNom(String nom) {
		this.nom = nom;
	}
	public String getPrenom() {
		return prenom;
	}
	public void setPrenom(String prenom) {
		this.prenom = prenom;
	}
	
	 
}
